# Print a header with a box
function print_header() {
    local title="$1"
    echo -e "┌───────────────────────────────┐"
    echo -e "│ $title │"
    echo -e "└───────────────────────────────┘"
}



#*
#* ┏==================================================================================================┓
#* ┃                                   📖 Printing Title                                              ┃
#* ┗==================================================================================================┛
#*


function print_title() {
    local title="$1"
    echo -e "== $title =="
}

#*
#* ┏==================================================================================================┓
#* ┃                                   📖 Prints Line Gap                                             ┃
#* ┗==================================================================================================┛
#*

function line_gap() {
    local lines=${1:-1}  # Default to 1 line if no argument given
    for ((i=0; i<lines; i++)); do
        echo -e ""
    done
}
