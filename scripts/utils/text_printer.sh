# Print a header with a box
printHeader() {
    local title="$1"
    echo -e "┌───────────────────────────────┐"
    echo -e "│ $title │"
    echo -e "└───────────────────────────────┘"
}

# Print a title (smaller)
printTitle() {
    local title="$1"
    echo -e "== $title =="
}
